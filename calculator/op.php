<?php
function sum($num1,$num2)
{
  $sum=$num1+$num2;
  echo"<h1>sum:{$sum}</h1>";
}
function sub($num1,$num2)
{
  $sub=$num1-$num2;
  echo"<h1>sub:{$sub}</h1>";
}

function div($num1,$num2)
{
  $div=$num1/$num2;
  echo"<h1>div:{$div}</h1>";
}

function mul($num1,$num2)
{
  $mul=$num1*$num2;
  echo"<h1>mul:{$mul}</h1>";
}

?>